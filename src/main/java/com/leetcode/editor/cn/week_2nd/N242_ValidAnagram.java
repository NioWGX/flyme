public class N242_ValidAnagram {

    public boolean isAnagram(String s, String t) {
        if (s == t){
            return true;
        }
        if (s == null || t == null){
            return false;
        }
        if (s.length() != t.length()){
            return false;
        }
        char[] sChars = s.toCharArray();
        char[] tChars = t.toCharArray();
        Arrays.sort(sChars);
        Arrays.sort(tChars);
        int idx = 0;
        while(idx < sChars.length){
            if (sChars[idx] != tChars[idx]){
                return false;
            }
            idx++;
        }
        return true;
    }
}
